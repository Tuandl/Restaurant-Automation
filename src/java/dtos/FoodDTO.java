package dtos;

import java.io.Serializable;

/**
 *
 * @author justdoit
 */
public class FoodDTO implements Serializable {
    private String id, name;
    private int price;

    public FoodDTO() {
    }

    public FoodDTO(String id, String name, int price) {
        this.id = id;
        this.name = name;
        this.price = price;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
    
    
}