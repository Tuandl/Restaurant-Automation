package dtos;

/**
 *
 * @author justdoit
 */
public class ManageHostDTO {
    String id, name;
    int action;

    public ManageHostDTO(String id, String name, int action) {
        this.id = id;
        this.name = name;
        this.action = action;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAction() {
        return action;
    }

    public void setAction(int action) {
        this.action = action;
    }
    
    
    
}